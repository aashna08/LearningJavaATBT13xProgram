package Test_Questions;

public class Ques004_inc_dec {
    public static void main(String[] args) {
        int a =10;
        System.out.println(a++); //Output 10 it will print the value first and then increase a value so value of a is 11
        System.out.println(++a); //Output 12 value of a is 11 and pre increment first increase the  value and then print it

        int b=20;
        System.out.println(b--); //20
        System.out.println(--b); //18
    }
}

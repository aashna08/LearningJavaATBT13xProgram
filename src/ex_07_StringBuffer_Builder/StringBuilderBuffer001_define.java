package ex_07_StringBuffer_Builder;

public class StringBuilderBuffer001_define {
    public static void main(String[] args) {
        {
            String s1="Aashna";
            String s2=new String("Aashna");
            StringBuffer s3=new StringBuffer("Aashna");
            StringBuilder s4=new StringBuilder("Aashna");
            System.out.println(s1);
            System.out.println(s2);
            System.out.println(s3);
            System.out.println(s4);
        }
    }
}
